public class CountingByHalves
{
	public static void main( String[] args )
	{
		System.out.println("x");
		System.out.println("------");
		double startingNumber = -10 ;
		
		for( ; startingNumber <= 10 ; startingNumber = startingNumber + 0.5){
			
			System.out.println(startingNumber);
			
		}
	}
}
		
		